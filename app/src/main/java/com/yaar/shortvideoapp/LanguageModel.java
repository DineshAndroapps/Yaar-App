package com.yaar.shortvideoapp;

public class LanguageModel {

    String language;
    String languageInEnglish;
    String languageCode;
    int icon;

    public LanguageModel(String language, String languageInEnglish, String languageCode, int icon) {
        this.language = language;
        this.languageInEnglish = languageInEnglish;
        this.languageCode = languageCode;
        this.icon = icon;
    }
}
